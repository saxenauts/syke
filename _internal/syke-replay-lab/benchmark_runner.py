def _judge_probe(
    *,
    item: dict[str, Any],
    answer_text: str,
    answer_metadata: dict[str, Any],
    slice_dir: Path,
    condition: str,
    ask_mode: str,
    memex_chars: int,
    judge_model: str | None,
    judge_provider: str | None,
    timeout_seconds: int,
    trace_dir: Path,
    evidence_dir: Path,
    shared_local_git_anchor: Path | None = None,
) -> dict[str, Any]:
    """Pi-based judge: verifies claims against the frozen slice data."""
    from memory_replay import temporary_workspace_binding

    from syke.db import SykeDB
    from syke.llm.backends import pi_ask as pi_ask_module
    from syke.llm.pi_client import resolve_pi_provider

    trace_dir.mkdir(parents=True, exist_ok=True)
    probe_id = str(item["probe_id"])
    run_id = f"{condition}_{probe_id}"

    with tempfile.TemporaryDirectory(prefix="syke-judge-") as tmp:
        tmpdir = Path(tmp)
        evidence_dir.mkdir(parents=True, exist_ok=True)

        local_git_anchor: Path | None = None
        persistent_git_anchor: Path | None = None
        if shared_local_git_anchor and shared_local_git_anchor.exists():
            local_git_anchor = tmpdir / "local_git_anchor.json"
            shutil.copy2(shared_local_git_anchor, local_git_anchor)
            persistent_git_anchor = evidence_dir / "local_git_anchor.json"
            shutil.copy2(shared_local_git_anchor, persistent_git_anchor)
        else:
            local_git_anchor = _build_local_git_anchor(item, tmpdir / "local_git_anchor.json")
            persistent_git_anchor = _build_local_git_anchor(
                item, evidence_dir / "local_git_anchor.json"
            )

        # Write packet with the real-ask evidence shape the judge should use.
        packet = _build_real_ask_packet(
            item=item,
            answer_text=answer_text,
            answer_metadata=answer_metadata,
            slice_dir=slice_dir,
            local_git_anchor=local_git_anchor,
            condition=condition,
            ask_mode=ask_mode,
            memex_chars=memex_chars,
        )
        (tmpdir / "packet.json").write_text(json.dumps(packet, indent=2), encoding="utf-8")
        (evidence_dir / "packet.json").write_text(json.dumps(packet, indent=2), encoding="utf-8")

        # Symlink the slice so the judge can browse raw harness data
        if slice_dir.exists():
            (tmpdir / "slice").symlink_to(slice_dir.resolve())
            if not (evidence_dir / "slice").exists():
                (evidence_dir / "slice").symlink_to(slice_dir.resolve())

        # Write schema for reference (included in prompt)
        schema_path = tmpdir / "judge_schema.json"
        schema_path.write_text(json.dumps(JUDGE_SCHEMA, indent=2), encoding="utf-8")

        # Build a minimal workspace for Pi in the judge tmpdir.
        # The judge doesn't need Syke identity or a memex -- it's evaluating.
        # It needs: syke.db (empty) and adapters/ (from slice).
        judge_scope = trace_dir.parent.parent.name
        judge_workspace = (
            Path.home()
            / ".syke-lab"
            / judge_scope
            / "judges"
            / f"{run_id}__{uuid.uuid4().hex[:8]}"
            / "workspace"
        )
        if judge_workspace.exists():
            shutil.rmtree(judge_workspace)
        judge_workspace.mkdir(parents=True, exist_ok=True)
        (judge_workspace / "sessions").mkdir(exist_ok=True)
        _write_judge_determinism_extension(
            judge_workspace,
            judge_provider=judge_provider,
        )

        # Copy slice adapters into the judge workspace
        slice_adapters = slice_dir / "adapters" if slice_dir.exists() else None
        judge_adapters = judge_workspace / "adapters"
        judge_adapters.mkdir(parents=True, exist_ok=True)
        if slice_adapters and slice_adapters.exists():
            for adapter in slice_adapters.glob("*.md"):
                shutil.copy2(adapter, judge_adapters / adapter.name)

        # Initialize syke.db (SykeDB constructor creates tables)
        db_path = judge_workspace / "syke.db"
        db = SykeDB(db_path)
        _write_reference_time_file(
            judge_workspace,
            reference_ts_local=str(item.get("reference_ts_local") or _reference_moment(item).isoformat()),
            reference_cutoff_iso=_reference_cutoff_iso(item),
        )
        frozen_time_bin = _prepare_frozen_time_tools(
            judge_workspace,
            reference_dt=_reference_moment(item),
        )

        scratch_sessions = judge_workspace.parent / ".judge_sessions"

        prompt = _build_judge_prompt(
            packet_path=tmpdir / "packet.json",
            slice_path=tmpdir / "slice",
            local_git_anchor=local_git_anchor,
        )
        full_prompt = prompt

        try:
            with temporary_workspace_binding(
                judge_workspace,
                sessions_dir=scratch_sessions,
                harness_paths=slice_dir,
            ):
                with _temporary_env(
                    {
                        "SYKE_PROVIDER": judge_provider,
                        "TZ": getattr(_reference_moment(item).tzinfo, "key", None)
                        or "America/Los_Angeles",
                        "SYKE_REFERENCE_TS_LOCAL": str(
                            item.get("reference_ts_local") or _reference_moment(item).isoformat()
                        ),
                        "SYKE_REFERENCE_CUTOFF_ISO": _reference_cutoff_iso(item),
                        "PATH": f"{frozen_time_bin}:{os.environ.get('PATH', '')}",
                    }
                ):
                    answer_text_raw, metadata = pi_ask_module.pi_ask(
                        db,
                        "user",
                        full_prompt,
                        timeout=timeout_seconds,
                        model=judge_model,
                        transport="benchmark_judge",
                        capture_trace=True,
                    )
                metadata = dict(metadata)

            # Write trace files
            trace_response = trace_dir / f"{run_id}.response.txt"
            trace_response.write_text(answer_text_raw, encoding="utf-8")
            trace_meta = trace_dir / f"{run_id}.metadata.json"
            trace_meta.write_text(json.dumps(metadata, indent=2, default=str), encoding="utf-8")
            (evidence_dir / "judge_response.txt").write_text(answer_text_raw, encoding="utf-8")
            (evidence_dir / "judge_metadata.json").write_text(
                json.dumps(metadata, indent=2, default=str), encoding="utf-8"
            )
            trace_payload = metadata.get("_trace_payload")
            if isinstance(trace_payload, dict):
                (trace_dir / f"{run_id}.judge_trace.json").write_text(
                    json.dumps(trace_payload, indent=2, default=str),
                    encoding="utf-8",
                )
                (evidence_dir / "judge_trace.json").write_text(
                    json.dumps(trace_payload, indent=2, default=str),
                    encoding="utf-8",
                )
            (evidence_dir / "index.json").write_text(
                json.dumps(
                    {
                        "probe_id": probe_id,
                        "condition": condition,
                        "slice_dir": str(slice_dir),
                        "packet_path": str(evidence_dir / "packet.json"),
                        "local_git_anchor": str(persistent_git_anchor)
                        if persistent_git_anchor
                        else None,
                    },
                    indent=2,
                ),
                encoding="utf-8",
            )

            result = _extract_tool_input(
                trace_payload if isinstance(trace_payload, dict) else None,
                tool_name="submit_judge_verdict",
            )
            if not isinstance(result, dict) or _extract_judge_json(json.dumps(result)) is None:
                result = _extract_judge_json(answer_text_raw)
            if result is not None:
                final_path = trace_dir / f"{run_id}.final.json"
                final_path.write_text(json.dumps(result, indent=2), encoding="utf-8")
                (evidence_dir / "judge_result.json").write_text(
                    json.dumps(result, indent=2), encoding="utf-8"
                )
                return result

            LOG.warning("eval %s: could not extract valid judge JSON from Pi response", run_id)
            return {
                "error": f"JSON extraction failed from Pi response (len={len(answer_text_raw)})"
            }

        except Exception as exc:
            LOG.exception("eval %s: Pi judge failed", run_id)
            return {"error": repr(exc)[:500]}
        finally:
            db.close()
            # Clean up judge workspace
            judge_ws_parent = judge_workspace.parent
            if judge_ws_parent.exists():
                shutil.rmtree(judge_ws_parent, ignore_errors=True)


def _extract_judge_json(text: str) -> dict[str, Any] | None:
    """Extract judge JSON from Pi's response text.

    Tries direct parse first, then regex extraction of the outermost
    JSON object. Returns None if no valid JSON with required keys found.
    """
    def _is_valid_dimension(payload: Any, *, subcategories: list[str]) -> bool:
        if not isinstance(payload, dict):
            return False
        if payload.get("score") not in {"strong", "partial", "missed"}:
            return False
        reasoning = payload.get("reasoning")
        if not isinstance(reasoning, str) or not reasoning.strip():
            return False
        subs = payload.get("subcategories")
        if not isinstance(subs, dict):
            return False
        if set(subs.keys()) != set(subcategories):
            return False
        for subcategory in subcategories:
            subpayload = subs.get(subcategory)
            if not isinstance(subpayload, dict):
                return False
            if subpayload.get("score") not in {"strong", "partial", "missed"}:
                return False
            sub_reasoning = subpayload.get("reasoning")
            if not isinstance(sub_reasoning, str) or not sub_reasoning.strip():
                return False
        return True

    def _is_valid_judge_result(payload: Any) -> bool:
        if not isinstance(payload, dict):
            return False
        if payload.get("overall_verdict") not in {"pass", "partial", "fail"}:
            return False
        summary = payload.get("summary")
        if not isinstance(summary, str) or not summary.strip():
            return False
        for axis_name, subcategories in JUDGE_AXIS_SUBCATEGORIES.items():
            if not _is_valid_dimension(payload.get(axis_name), subcategories=subcategories):
                return False
        return True

    # Try direct parse
    stripped = text.strip()
    if stripped.startswith("{"):
        try:
            obj = json.loads(stripped)
            if _is_valid_judge_result(obj):
                return obj
        except json.JSONDecodeError:
            pass

    # Regex: find the outermost {...} block
    match = re.search(r"\{[\s\S]*\}", text)
    if match:
        try:
            obj = json.loads(match.group())
            if _is_valid_judge_result(obj):
                return obj
        except json.JSONDecodeError:
            pass

    return None


def _extract_tool_input(
    trace_payload: dict[str, Any] | None,
    *,
    tool_name: str,
) -> dict[str, Any] | None:
    if not isinstance(trace_payload, dict):
        return None
    tool_calls = trace_payload.get("tool_calls_detail")
    if not isinstance(tool_calls, list):
        return None
    for tool_call in reversed(tool_calls):
        if not isinstance(tool_call, dict):
            continue
        if str(tool_call.get("name") or "") != tool_name:
            continue
        payload = tool_call.get("input")
        if isinstance(payload, dict):
            return payload
    return None


def _reduce_verdict(judge_result: dict[str, Any]) -> str:
    """Map judge output to a single verdict: pass / partial / fail / invalid."""
    if "error" in judge_result:
        return "invalid"
    return str(judge_result.get("overall_verdict") or "fail")


def _persist_ask_trace(
    *,
    probe_id: str,
    condition: str,
    answer_text: str,
    answer_metadata: dict[str, Any],
    trace_dir: Path,
    evidence_dir: Path,
) -> dict[str, Any]:
    """Persist benchmark ask-side rollout traces without writing into syke.db."""
    trace_dir.mkdir(parents=True, exist_ok=True)
    evidence_dir.mkdir(parents=True, exist_ok=True)
    run_id = f"{condition}_{probe_id}"

    metadata = dict(answer_metadata)
    prompt_text = metadata.pop("_input_text", None)
    trace_payload = metadata.pop("_trace_payload", None)

    (trace_dir / f"{run_id}.ask_response.txt").write_text(answer_text, encoding="utf-8")
    (trace_dir / f"{run_id}.ask_metadata.json").write_text(
        json.dumps(metadata, indent=2, default=str),
        encoding="utf-8",
    )
    (evidence_dir / "ask_response.txt").write_text(answer_text, encoding="utf-8")
    (evidence_dir / "ask_metadata.json").write_text(
        json.dumps(metadata, indent=2, default=str),
        encoding="utf-8",
    )

    if prompt_text:
        (trace_dir / f"{run_id}.ask_prompt.txt").write_text(prompt_text, encoding="utf-8")
        (evidence_dir / "ask_prompt.txt").write_text(prompt_text, encoding="utf-8")

    if isinstance(trace_payload, dict):
        (trace_dir / f"{run_id}.ask_trace.json").write_text(
            json.dumps(trace_payload, indent=2, default=str),
            encoding="utf-8",
        )
        (evidence_dir / "ask_trace.json").write_text(
            json.dumps(trace_payload, indent=2, default=str),
            encoding="utf-8",
        )

    return metadata


