def _find_cycle_matched_memex(timeline: list[dict[str, Any]], reference_dt: datetime) -> str:
    """Return memex content from the latest cycle whose cutoff ≤ reference_dt.

    Requires exact `cycle_cutoff_iso` matching. Legacy day-bounded replay
    timelines are rejected for eval because they permit time leakage.
    Returns empty string for empty timeline (null baseline).
    """
    _validate_timefixed_timeline(timeline)
    matched: dict[str, Any] | None = None
    for cycle in timeline:
        cutoff_iso = cycle.get("cycle_cutoff_iso")
        try:
            cutoff_dt = datetime.fromisoformat(str(cutoff_iso))
        except ValueError:
            cutoff_dt = None
        if cutoff_dt is not None and cutoff_dt.tzinfo is None:
            cutoff_dt = cutoff_dt.replace(tzinfo=reference_dt.tzinfo)
        if cutoff_dt is not None and cutoff_dt <= reference_dt:
            matched = cycle
    return (matched.get("memex_content") or "") if matched else ""


def _inject_memex(
    db_path: Path,
    content: str,
    *,
    timestamp_override: str | None = None,
) -> None:
    """Replace the active memex row in a workspace's syke.db."""
    import sqlite3

    from uuid_extensions import uuid7

    now_iso = timestamp_override or datetime.now(UTC).isoformat()
    conn = sqlite3.connect(str(db_path))
    try:
        conn.execute(
            "UPDATE memories SET active = 0 "
            "WHERE user_id = ? AND json_extract(source_event_ids, '$[0]') = '__memex__'",
            ("user",),
        )
        if content:
            conn.execute(
                "INSERT INTO memories ("
                "id, user_id, content, source_event_ids, created_at, updated_at, active"
                ") "
                "VALUES (?, ?, ?, ?, ?, ?, 1)",
                (
                    str(uuid7()),
                    "user",
                    content,
                    '["__memex__"]',
                    now_iso,
                    now_iso,
                ),
            )
        conn.commit()
    finally:
        conn.close()


def _load_slice_meta(slice_dir: Path) -> dict[str, Any] | None:
    path = slice_dir / "slice_meta.json"
    if not path.exists():
        return None
    try:
        return json.loads(path.read_text(encoding="utf-8"))
    except Exception:
        return None


def _summarize_slice_meta(slice_meta: dict[str, Any] | None) -> dict[str, Any]:
    if not isinstance(slice_meta, dict):
        return {}
    sources = slice_meta.get("sources") or {}
    source_summary = {}
    for name, stats in sources.items():
        if not isinstance(stats, dict):
            continue
        source_summary[str(name)] = {
            "jsonl_files": int(stats.get("jsonl_files") or 0),
            "jsonl_lines": int(stats.get("jsonl_lines") or 0),
            "db_rows": stats.get("db_rows") or {},
        }
    return {
        "cycle": slice_meta.get("cycle"),
        "bundle": slice_meta.get("bundle"),
        "sources": source_summary,
        "total_elapsed_sec": slice_meta.get("total_elapsed_sec"),
    }


def _build_judge_brief(item: dict[str, Any]) -> dict[str, Any]:
    return {
        "object": "Restore enough of the user's live working model at time t for useful continuation.",
        "useful_means": [
            "re-enter the right live thread",
            "identify what changed and what is still live",
            "surface the right artifact or restart path",
            "reduce reconstruction work without overclaiming",
        ],
        "partial_means": [
            "some useful reconstruction happened but important entropy remains",
            "timeline or artifact routing is fuzzy",
            "stale residue or weak salience remains in the answer",
        ],
        "fail_means": [
            "stale or future state is presented as current",
            "major active thread or artifact is missed",
            "cross-source merge is fabricated or collapses important distinctions",
            "answer is generic rather than restart-capable",
        ],
    }


def _build_real_ask_packet(
    *,
    item: dict[str, Any],
    answer_text: str,
    answer_metadata: dict[str, Any],
    slice_dir: Path,
    local_git_anchor: Path | None,
    condition: str,
    ask_mode: str,
    memex_chars: int,
) -> dict[str, Any]:
    slice_meta = _load_slice_meta(slice_dir)
    packet = {
        "probe": {
            "probe_id": str(item["probe_id"]),
            "question": item.get("prompt_text") or item.get("question", ""),
            "family": item.get("family", ""),
            "reference_dt": str(item.get("reference_dt") or ""),
            "reference_ts_local": str(item.get("reference_ts_local") or ""),
            "reference_cutoff_iso": _reference_cutoff_iso(item),
            "source_surface": item.get("source_surface") or item.get("source") or "",
            "source_ref": item.get("source_ref") or "",
        },
        "answer": {"text": answer_text, "metadata": answer_metadata},
        "raw_context": {
            "slice_dir": str(slice_dir),
            "slice_summary": _summarize_slice_meta(slice_meta),
            "replay_state": {
                "condition": condition,
                "ask_mode": ask_mode,
                "memex_chars": memex_chars,
            },
        },
        "local_git_set": {
            "available": bool(local_git_anchor and local_git_anchor.exists()),
            "path": str(local_git_anchor) if local_git_anchor else None,
        },
        "judge_brief": _build_judge_brief(item),
    }
    return packet


def _reference_now_string(
    *,
    reference_ts_local: str,
    reference_cutoff_iso: str,
) -> str:
    """Human-readable 'now' string with the ISO cutoff as the unambiguous anchor.

    Feeds into the <now> block carried by build_prompt. Using the local form
    plus the ISO keeps both a reader-friendly and a machine-precise form in
    the same line.
    """
    return f"{reference_ts_local} (cutoff {reference_cutoff_iso})"


def _write_reference_time_file(
    workspace_root: Path,
    *,
    reference_ts_local: str,
    reference_cutoff_iso: str,
) -> None:
    """Filesystem-surface defense in depth.

    The <now> block in the prompt is the primary authority. This file exists
    so that an agent which shells/greps the workspace still finds the same
    directive instead of falling back to host clock or mtimes.
    """
    body = (
        f"As-of local time: {reference_ts_local}\n"
        f"As-of cutoff ISO: {reference_cutoff_iso}\n"
        "Treat this as the authoritative current time for this task.\n"
        "Resolve relative time words like today, yesterday, last, now, and most recent against this as-of time.\n"
        "Do not use wall-clock time, file mtimes, or host system date commands as truth.\n"
    )
    (workspace_root / "REFERENCE_TIME.md").write_text(body, encoding="utf-8")


def _prepare_frozen_time_tools(
    workspace_root: Path,
    *,
    reference_dt: datetime,
) -> Path:
    bin_dir = workspace_root / ".time-sandbox" / "bin"
    bin_dir.mkdir(parents=True, exist_ok=True)
    epoch = int(reference_dt.timestamp())
    tz_name = getattr(reference_dt.tzinfo, "key", None) or "America/Los_Angeles"
    script = (
        "#!/bin/sh\n"
        f'EPOCH="{epoch}"\n'
        f'TZ_LOCAL="{tz_name}"\n'
        'if [ "${1:-}" = "-u" ]; then\n'
        "  shift\n"
        '  TZ=UTC /bin/date -r "$EPOCH" "$@"\n'
        "else\n"
        '  TZ="$TZ_LOCAL" /bin/date -r "$EPOCH" "$@"\n'
        "fi\n"
    )
    date_path = bin_dir / "date"
    date_path.write_text(script, encoding="utf-8")
    date_path.chmod(0o755)
    return bin_dir


