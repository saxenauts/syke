def _reuse_ask_artifacts(
    *,
    row: dict[str, Any],
    probe_id: str,
    condition: str,
    trace_dir: Path,
    evidence_dir: Path,
) -> dict[str, Any]:
    """Copy ask-side artifacts from an existing benchmark run into a new run."""
    trace_dir.mkdir(parents=True, exist_ok=True)
    evidence_dir.mkdir(parents=True, exist_ok=True)

