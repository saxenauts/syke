def _build_probe_workspace(
    *,
    bundle_path: Path,
    item: dict[str, Any],
    memex_content: str,
    output_dir: Path,
    condition: str,
    slice_dir: Path,
) -> tuple[Path, Path]:
    """Build a fresh, isolated workspace for one (eval, condition) run.

    Returns (workspace_root, slice_dir). Caller must clean up.
    """
    from memory_replay import configure_bundle_workspace, rewire_adapters_to_slice

    probe_id = str(item["probe_id"])
    run_root = _run_root_for_output(output_dir)
    # configure_bundle_workspace uses only probe_dir.name as the workspace
    # directory name under ~/.syke-lab/. Use a unique per-eval name so
    # parallel workers never reuse the same sqlite workspace root.
    workspace_token = uuid.uuid4().hex[:8]
    probe_dir = run_root / "workspaces" / f"{condition}__{probe_id}__{workspace_token}"

    # 1. Fresh workspace via configure_bundle_workspace
    workspace_root, db_path = configure_bundle_workspace(probe_dir, bundle_path)

    # 2. Initialize the db schema (SykeDB constructor creates tables)
    from syke.db import SykeDB

    _db = SykeDB(db_path)
    _db.close()

    # 3. Rewire adapters to point at the shared slice
    rewire_adapters_to_slice(workspace_root, slice_dir)

    # 4. Inject cycle-matched memex (empty string for the null baseline)
    reference_moment = _reference_moment(item)
    _inject_memex(
        workspace_root / "syke.db",
        memex_content,
        timestamp_override=reference_moment.isoformat(),
    )
    _write_reference_time_file(
        workspace_root,
        reference_ts_local=str(item.get("reference_ts_local") or reference_moment.isoformat()),
        reference_cutoff_iso=_reference_cutoff_iso(item),
    )
    _prepare_frozen_time_tools(workspace_root, reference_dt=reference_moment)

    return workspace_root, slice_dir


def _build_ask_prompt(
    *,
    workspace_root: Path,
    db: Any,
    user_id: str,
    question: str,
    ask_mode: str,
    reference_ts_local: str,
    reference_cutoff_iso: str,
    synthesis_path: Path | None = None,
) -> str:
    """Funnel all three ask modes through build_prompt.

    Time authority is the probe's reference cutoff for every mode, carried
    by the <now> block inside build_prompt. Modes differ only in which
    memory surfaces are included:
      pure → <psyche> + <now>
      zero → <psyche> + <now> + <memex>
      syke → <psyche> + <now> + <memex> + <synthesis>
    """
    from syke.runtime.psyche_md import build_prompt

    now_str = _reference_now_string(
        reference_ts_local=reference_ts_local,
        reference_cutoff_iso=reference_cutoff_iso,
    )

    if ask_mode == "pure":
        base = build_prompt(
            workspace_root,
            home=workspace_root,
            now=now_str,
            include_memex=False,
            include_synthesis=False,
        )
    elif ask_mode == "zero":
        base = build_prompt(
            workspace_root,
            db=db,
            user_id=user_id,
            home=workspace_root,
            now=now_str,
            include_memex=True,
            include_synthesis=False,
        )
    elif ask_mode == "syke":
        base = build_prompt(
            workspace_root,
            db=db,
            user_id=user_id,
            home=workspace_root,
            synthesis_path=synthesis_path,
            now=now_str,
            include_memex=True,
            include_synthesis=True,
        )
    else:
        raise ValueError(f"Unknown ask mode: {ask_mode}")

    return f"{base}\n---\n\nUser question: {question}"


