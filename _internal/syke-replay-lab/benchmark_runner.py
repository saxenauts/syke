def _item_map(items_path: Path | None = None) -> dict[str, dict[str, Any]]:
    payload = _load_yaml(items_path or ITEMS_PATH)
    return {str(item["probe_id"]): dict(item) for item in (payload.get("items") or [])}


def _runset_item_ids(name: str, runsets_path: Path | None = None) -> list[str]:
    payload = _load_yaml(runsets_path or RUNSETS_PATH)
    runsets = payload.get("runsets") or {}
    if name not in runsets:
        raise KeyError(f"Unknown runset: {name}")
    entry = runsets[name]
    if "items" in entry:
        return [str(i) for i in entry["items"]]
    if "items_by_dataset" in entry:
        out: list[str] = []
        for items in entry["items_by_dataset"].values():
            out.extend(str(i) for i in items)
        return out
    raise ValueError(f"Runset has no items: {name}")


def _select_items(
    item_ids: list[str] | None,
    runset: str | None,
    *,
    items_path: Path | None = None,
    runsets_path: Path | None = None,
    all_items: bool = False,
) -> list[dict[str, Any]]:
    items = _item_map(items_path)
    if item_ids:
        return [items[i] for i in item_ids]
    if runset:
        return [items[i] for i in _runset_item_ids(runset, runsets_path)]
    if all_items:
        return list(items.values())
    raise ValueError("Provide --item, --runset, or --all-items")


def _bundle_for_dataset(dataset_id: str, primary_dataset_id: str | None = None) -> Path:
    if dataset_id == "NE-1.2":
        raise ValueError("NE-1.2 is a permanent dead zone; no bundle")
    if dataset_id == "cross-dataset":
        if not primary_dataset_id:
            raise ValueError("cross-dataset eval requires primary_dataset_id")
        return _bundle_for_dataset(primary_dataset_id)
    return DATASET_TO_BUNDLE[dataset_id]


def _routing_dataset_id(item: dict[str, Any]) -> str:
    ds = str(item["dataset_id"])
    if ds == "cross-dataset":
        primary = item.get("primary_dataset_id")
        if not primary:
            raise ValueError(
                f"cross-dataset eval {item.get('probe_id')} missing primary_dataset_id"
            )
        return str(primary)
    return ds


def _reference_moment(item: dict[str, Any]) -> datetime:
    """Return the intended local reference moment for an eval item.

    Prefer the exact ask-local timestamp when present. Fall back to end-of-day
    local time for older items that only have a date.
    """
    raw_local = item.get("reference_ts_local")
    if raw_local:
        for fmt in ("%Y-%m-%d %H:%M %Z", "%Y-%m-%d %H:%M:%S %Z"):
            try:
                parsed = datetime.strptime(str(raw_local), fmt)
                if parsed.tzinfo is None:
                    parsed = parsed.replace(tzinfo=ZoneInfo("America/Los_Angeles"))
                return parsed
            except ValueError:
                continue
    raw = item.get("reference_dt")
    if not raw:
        raise ValueError(f"eval {item.get('probe_id')} has no reference_dt")
    return datetime.strptime(str(raw), "%Y-%m-%d").replace(
        hour=23, minute=59, second=59, tzinfo=ZoneInfo("America/Los_Angeles")
    )


def _reference_cutoff_iso(item: dict[str, Any]) -> str:
    return _reference_moment(item).isoformat()


