# ── Cycle-matched memex ──


def _find_cycle_matched_memex(timeline: list[dict[str, Any]], reference_dt: datetime) -> str:
    """Return memex content from the latest cycle whose cutoff ≤ reference_dt.

    Requires exact `cycle_cutoff_iso` matching. Legacy day-bounded replay
    timelines are rejected for eval because they permit time leakage.
    Returns empty string for empty timeline (null baseline).
    """
    _validate_timefixed_timeline(timeline)
    matched: dict[str, Any] | None = None
    for cycle in timeline:
        cutoff_iso = cycle.get("cycle_cutoff_iso")
        try:
            cutoff_dt = datetime.fromisoformat(str(cutoff_iso))
        except ValueError:
            cutoff_dt = None
        if cutoff_dt is not None and cutoff_dt.tzinfo is None:
            cutoff_dt = cutoff_dt.replace(tzinfo=reference_dt.tzinfo)
        if cutoff_dt is not None and cutoff_dt <= reference_dt:
            matched = cycle
    return (matched.get("memex_content") or "") if matched else ""


def _inject_memex(db_path: Path, content: str) -> None:
    """Replace the active memex row in a workspace's syke.db."""
    import sqlite3

    from uuid_extensions import uuid7

    conn = sqlite3.connect(str(db_path))
    try:
        conn.execute(
            "UPDATE memories SET active = 0 "
            "WHERE user_id = ? AND json_extract(source_event_ids, '$[0]') = '__memex__'",
            ("user",),
        )
        if content:
            conn.execute(
                "INSERT INTO memories ("
                "id, user_id, content, source_event_ids, created_at, updated_at, active"
                ") "
                "VALUES (?, ?, ?, ?, ?, ?, 1)",
                (
                    str(uuid7()),
                    "user",
                    content,
                    '["__memex__"]',
                    datetime.now(UTC).isoformat(),
                    datetime.now(UTC).isoformat(),
                ),
            )
        conn.commit()
    finally:
        conn.close()


# ── Workspace construction ──


def _build_probe_workspace(
    *,
    bundle_path: Path,
    item: dict[str, Any],
    memex_content: str,
    output_dir: Path,
    condition: str,
) -> tuple[Path, Path]:
    """Build a fresh, isolated workspace for one (eval, condition) run.

    Returns (workspace_root, slice_dir). Caller must clean up.
    """
    from cycle_slicer import slice_bundle
    from memory_replay import configure_bundle_workspace, rewire_adapters_to_slice

    probe_id = str(item["probe_id"])
    run_root = Path.home() / ".syke-lab" / output_dir.name
    # configure_bundle_workspace uses only probe_dir.name as the workspace
    # directory name under ~/.syke-lab/. Use a unique per-eval name so
    # parallel workers never reuse the same sqlite workspace root.
    workspace_token = uuid.uuid4().hex[:8]
    probe_dir = run_root / "workspaces" / f"{condition}__{probe_id}__{workspace_token}"

    # 1. Slice raw data to eval's reference moment.
    # Keep slices condition-scoped so parallel workers for the same probe do
    # not race on a shared directory during materialization.
    reference_moment = _reference_moment(item)
    slice_dir = run_root / "slices" / f"{condition}__{probe_id}"
    if not slice_dir.exists():
        slice_bundle(bundle_path, reference_moment, slice_dir)

    # 2. Fresh workspace via configure_bundle_workspace
    workspace_root, db_path = configure_bundle_workspace(probe_dir, bundle_path)

    # 3. Initialize the db schema (SykeDB constructor creates tables)
    from syke.db import SykeDB

    _db = SykeDB(db_path)
    _db.close()

    # 4. Rewire adapters to point at the slice
    rewire_adapters_to_slice(workspace_root, slice_dir)

    # 5. Inject cycle-matched memex (empty string for the null baseline)
    _inject_memex(workspace_root / "syke.db", memex_content)

    return workspace_root, slice_dir


def _build_identity_only_prompt(*, workspace_root: Path, question: str) -> str:
    from syke.runtime.psyche_md import _build_psyche_md

    psyche = _build_psyche_md(workspace_root, home=workspace_root)
    return f"{psyche}\n---\n\nUser question: {question}"


def _build_substrate_only_prompt(
    *, workspace_root: Path, db: Any, user_id: str, question: str
) -> str:
    from syke.runtime.psyche_md import _build_memex_block, _build_psyche_md

    psyche = _build_psyche_md(workspace_root, home=workspace_root)
    memex = _build_memex_block(db, user_id, context="ask")
    return f"{psyche}{memex}\n---\n\nUser question: {question}"


def _build_ask_prompt(
    *,
    workspace_root: Path,
    db: Any,
    user_id: str,
    question: str,
    ask_mode: str,
    synthesis_path: Path | None = None,
) -> str:
    from syke.runtime.psyche_md import build_prompt

    if ask_mode == "pure":
        return _build_identity_only_prompt(workspace_root=workspace_root, question=question)
    if ask_mode == "zero":
        return _build_substrate_only_prompt(
            workspace_root=workspace_root,
            db=db,
            user_id=user_id,
            question=question,
        )
    if ask_mode == "syke":
        base = build_prompt(
            workspace_root,
            db=db,
            user_id=user_id,
            home=workspace_root,
            synthesis_path=synthesis_path,
        )
        return f"{base}\n---\n\nUser question: {question}"
    raise ValueError(f"Unknown ask mode: {ask_mode}")


