@contextlib.contextmanager
def _temporary_env(overrides: dict[str, str | None]):
    old = {}
    try:
        for key, value in overrides.items():
            old[key] = os.environ.get(key)
            if value is None:
                os.environ.pop(key, None)
            else:
                os.environ[key] = value
        yield
    finally:
        for key, value in old.items():
            if value is None:
                os.environ.pop(key, None)
            else:
                os.environ[key] = value


def _write_json_atomic(path: Path, payload: Any) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    tmp = path.with_name(f"{path.name}.tmp")
    tmp.write_text(json.dumps(payload, indent=2, default=str), encoding="utf-8")
    tmp.replace(path)


def _load_yaml(path: Path) -> dict[str, Any]:
    try:
        import yaml

        return json.loads(
            json.dumps(yaml.safe_load(path.read_text(encoding="utf-8")) or {}, default=str)
        )
    except ModuleNotFoundError:
        for exe in ["/opt/homebrew/bin/python3", "/usr/local/bin/python3", "/usr/bin/python3"]:
            if not Path(exe).exists():
                continue
            r = subprocess.run(
                [
                    exe,
                    "-c",
                    (
                        "import json,sys,yaml; "
                        "print(json.dumps(yaml.safe_load(open(sys.argv[1])) or {}, default=str))"
                    ),
                    str(path),
                ],
                text=True,
                capture_output=True,
                check=False,
            )
            if r.returncode == 0 and r.stdout.strip():
                return dict(json.loads(r.stdout))
        raise


def _items(payload: Any) -> list[dict[str, Any]]:
    if isinstance(payload, list):
        return [row for row in payload if isinstance(row, dict)]
    if isinstance(payload, dict):
        if isinstance(payload.get("items"), list):
            return [row for row in payload["items"] if isinstance(row, dict)]
        if isinstance(payload.get("results"), list):
            return [row for row in payload["results"] if isinstance(row, dict)]
    return []


def _is_success(verdict: str) -> bool:
    return verdict == "pass"


def _canonical_condition_name(name: str) -> str:
    normalized = name.strip()
    if not normalized:
        raise ValueError("Condition name cannot be empty.")
    return normalized


def _ask_mode_for_condition(name: str) -> str:
    lowered = _canonical_condition_name(name).lower()
    if lowered == "pure":
        return "pure"
    if lowered == "zero":
        return "zero"
    return "syke"


def _validate_timefixed_timeline(
    timeline: list[dict[str, Any]], *, replay_path: Path | None = None
) -> None:
    missing = [
        str(cycle.get("cycle") or idx)
        for idx, cycle in enumerate(timeline, 1)
        if not cycle.get("cycle_cutoff_iso")
    ]
    if missing:
        sample = ", ".join(missing[:5])
        where = f" in {replay_path}" if replay_path else ""
        raise ValueError(
            "Replay results must be timefixed for eval; missing cycle_cutoff_iso"
            f" for cycles {sample}{where}."
        )


def _load_replay_condition_spec(
    *,
    condition: str,
    replay_dir: Path,
    synthesis_path: str | None,
) -> dict[str, Any]:
    replay_results = replay_dir / "replay_results.json"
    if not replay_results.exists():
        raise ValueError(f"Replay results not found: {replay_results}")

    payload = json.loads(replay_results.read_text(encoding="utf-8"))
    timeline = list(payload.get("timeline") or [])
    _validate_timefixed_timeline(timeline, replay_path=replay_results)

    metadata = payload.get("metadata") or {}
    replay_condition_raw = _canonical_condition_name(str(metadata.get("condition") or ""))
    if replay_condition_raw != condition:
        raise ValueError(
            f"Eval condition {condition!r} does not match replay source condition "
            f"{replay_condition_raw!r} in {replay_results}."
        )
    ask_mode = _ask_mode_for_condition(replay_condition_raw)
    skill_content = str(metadata.get("skill_content") or "")
    if ask_mode == "syke" and not skill_content:
        raise ValueError(
            f"Replay source {replay_results} is missing skill_content for syke-mode eval condition "
            f"{replay_condition_raw!r}."
        )

    return {
        "timeline": timeline,
        "ask_mode": ask_mode,
        "replay_source": str(replay_dir.resolve()),
        "synthesis_path": synthesis_path,
        "replay_condition": replay_condition_raw,
        "skill_content": skill_content,
    }


def _summarize_results(results: list[dict[str, Any]]) -> dict[str, dict[str, Any]]:
    by_condition: dict[str, dict[str, Any]] = {}
    for row in results:
        condition = str(row["condition"])
        bucket = by_condition.setdefault(
            condition,
            {
                "counts": Counter(),
                "judged": 0,
                "successes": 0,
                "zero_search_successes": 0,
                "tool_calls": [],
                "cost_usd": [],
                "duration_ms": [],
                "input_tokens": [],
                "output_tokens": [],
                "cache_read_tokens": [],
                "total_tool_calls": 0,
                "total_cost_usd": 0.0,
                "total_duration_ms": 0,
                "total_input_tokens": 0,
                "total_output_tokens": 0,
                "total_cache_read_tokens": 0,
            },
        )
        verdict = str(row.get("verdict") or "invalid")
        bucket["counts"][verdict] += 1
        if verdict != "invalid":
            bucket["judged"] += 1
            if _is_success(verdict):
                bucket["successes"] += 1
                if bool(row.get("zero_search")):
                    bucket["zero_search_successes"] += 1
        answer_metadata = row.get("answer_metadata") or {}
        tool_calls = int(row.get("tool_calls") or 0)
        cost_usd = float(row.get("cost_usd") or 0.0)
        duration_ms = int(answer_metadata.get("duration_ms") or 0)
        input_tokens = int(answer_metadata.get("input_tokens") or 0)
        output_tokens = int(answer_metadata.get("output_tokens") or 0)
        cache_read_tokens = int(answer_metadata.get("cache_read_tokens") or 0)
        bucket["tool_calls"].append(tool_calls)
        bucket["cost_usd"].append(cost_usd)
        bucket["duration_ms"].append(duration_ms)
        bucket["input_tokens"].append(input_tokens)
        bucket["output_tokens"].append(output_tokens)
        bucket["cache_read_tokens"].append(cache_read_tokens)
        bucket["total_tool_calls"] += tool_calls
        bucket["total_cost_usd"] += cost_usd
        bucket["total_duration_ms"] += duration_ms
        bucket["total_input_tokens"] += input_tokens
        bucket["total_output_tokens"] += output_tokens
        bucket["total_cache_read_tokens"] += cache_read_tokens

    summary: dict[str, dict[str, Any]] = {}
    for condition, bucket in by_condition.items():
        judged = int(bucket["judged"])
        successes = int(bucket["successes"])
        total_tool_calls = int(bucket["total_tool_calls"])
        total_cost_usd = float(bucket["total_cost_usd"])
        total_duration_ms = int(bucket["total_duration_ms"])
        total_input_tokens = int(bucket["total_input_tokens"])
        total_output_tokens = int(bucket["total_output_tokens"])
        total_cache_read_tokens = int(bucket["total_cache_read_tokens"])
        total_tokens = total_input_tokens + total_output_tokens
        summary[condition] = {
            "counts": dict(bucket["counts"]),
            "judged": judged,
            "successes": successes,
            "success_rate": (successes / judged) if judged else 0.0,
            "zero_search_success_rate": (
                int(bucket["zero_search_successes"]) / judged if judged else 0.0
            ),
            "tool_calls_per_success": (total_tool_calls / successes) if successes else 0.0,
            "cost_per_success": (total_cost_usd / successes) if successes else 0.0,
            "duration_ms_per_success": (total_duration_ms / successes) if successes else 0.0,
            "tokens_per_success": (total_tokens / successes) if successes else 0.0,
            "total_tool_calls": total_tool_calls,
            "total_cost_usd": total_cost_usd,
            "total_duration_ms": total_duration_ms,
            "total_input_tokens": total_input_tokens,
            "total_output_tokens": total_output_tokens,
            "total_cache_read_tokens": total_cache_read_tokens,
            "total_tokens": total_tokens,
            "avg_tool_calls": mean(bucket["tool_calls"]) if bucket["tool_calls"] else 0.0,
            "avg_cost_usd": mean(bucket["cost_usd"]) if bucket["cost_usd"] else 0.0,
            "avg_duration_ms": mean(bucket["duration_ms"]) if bucket["duration_ms"] else 0.0,
            "avg_input_tokens": mean(bucket["input_tokens"]) if bucket["input_tokens"] else 0.0,
            "avg_output_tokens": mean(bucket["output_tokens"]) if bucket["output_tokens"] else 0.0,
            "avg_cache_read_tokens": mean(bucket["cache_read_tokens"])
            if bucket["cache_read_tokens"]
            else 0.0,
            "avg_total_tokens": (
                mean(
                    [
                        i + o
                        for i, o in zip(
                            bucket["input_tokens"],
                            bucket["output_tokens"],
                            strict=False,
                        )
                    ]
                )
                if bucket["input_tokens"] and bucket["output_tokens"]
                else 0.0
            ),
        }
    return summary


