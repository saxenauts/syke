def _write_judge_determinism_extension(
    judge_workspace: Path, *, judge_provider: str | None
) -> None:
    extensions_dir = judge_workspace / ".pi" / "extensions"
    extensions_dir.mkdir(parents=True, exist_ok=True)
    payload_override = (
        "{ ...event.payload, temperature: 0 }"
        if judge_provider == "anthropic"
        else "event.payload"
    )
    (extensions_dir / "judge_determinism.ts").write_text(
        (
            "export default function (pi) {\n"
            "  pi.on(\"before_provider_request\", (event) => (\n"
            f"    {payload_override}\n"
            "  ));\n"
            "}\n"
        ),
        encoding="utf-8",
    )


# ── Ask ──


def _ask_probe(
    *,
    workspace_root: Path,
    slice_dir: Path,
    item: dict[str, Any],
    timeout_seconds: int,
    ask_mode: str,
    ask_model: str | None,
    synthesis_path: Path | None,
) -> tuple[str, dict[str, Any]]:
    """Run pi_ask against an isolated eval workspace. Returns (answer_text, metadata)."""
    from memory_replay import temporary_workspace_binding

    from syke.db import SykeDB
    from syke.llm.backends import pi_ask as pi_ask_module

    scratch_sessions = workspace_root.parent / ".ask_sessions"
    with temporary_workspace_binding(
        workspace_root,
        sessions_dir=scratch_sessions,
        harness_paths=slice_dir,
    ):
        db = SykeDB(workspace_root / "syke.db")
        try:
            question = item.get("prompt_text") or item.get("question", "")
            full_question = _build_ask_prompt(
                workspace_root=workspace_root,
                db=db,
                user_id="user",
                question=str(question),
                ask_mode=ask_mode,
                synthesis_path=synthesis_path,
            )
            answer, metadata = pi_ask_module.pi_ask(
                db,
                "user",
                full_question,
                timeout=timeout_seconds,
                model=ask_model,
                transport="benchmark",
                capture_trace=True,
            )
            return answer, dict(metadata)
        finally:
            db.close()


# ── Judge ──


def _judge_probe(
    *,
    item: dict[str, Any],
    answer_text: str,
    answer_metadata: dict[str, Any],
    slice_dir: Path,
    condition: str,
    judge_model: str | None,
    timeout_seconds: int,
    trace_dir: Path,
    evidence_dir: Path,
) -> dict[str, Any]:
    """Pi-based judge: verifies claims against the frozen slice data."""
    from memory_replay import temporary_workspace_binding

    from syke.db import SykeDB
    from syke.llm.backends import pi_ask as pi_ask_module
    from syke.llm.pi_client import resolve_pi_provider

    trace_dir.mkdir(parents=True, exist_ok=True)
    probe_id = str(item["probe_id"])
    run_id = f"{condition}_{probe_id}"

    with tempfile.TemporaryDirectory(prefix="syke-judge-") as tmp:
        tmpdir = Path(tmp)
        evidence_dir.mkdir(parents=True, exist_ok=True)

        # Write packet with the real-ask evidence shape the judge should use.
        packet = {
            "probe": {
                "probe_id": probe_id,
                "question": item.get("prompt_text") or item.get("question", ""),
                "family": item.get("family", ""),
                "reference_dt": str(item.get("reference_dt") or ""),
                "reference_ts_local": str(item.get("reference_ts_local") or ""),
                "reference_cutoff_iso": _reference_cutoff_iso(item),
                "source_surface": item.get("source_surface") or item.get("source") or "",
                "source_ref": item.get("source_ref") or "",
                "must_recover": list(item.get("must_recover") or []),
                "judge_focus": list(item.get("judge_focus") or []),
            },
            "answer": {"text": answer_text, "metadata": answer_metadata},
        }
        (tmpdir / "packet.json").write_text(json.dumps(packet, indent=2), encoding="utf-8")
        (evidence_dir / "packet.json").write_text(json.dumps(packet, indent=2), encoding="utf-8")

        # Symlink the slice so the judge can browse raw harness data
        if slice_dir.exists():
            (tmpdir / "slice").symlink_to(slice_dir.resolve())
            if not (evidence_dir / "slice").exists():
                (evidence_dir / "slice").symlink_to(slice_dir.resolve())

        local_git_anchor = _build_local_git_anchor(item, tmpdir / "local_git_anchor.json")
        persistent_git_anchor = _build_local_git_anchor(
            item, evidence_dir / "local_git_anchor.json"
        )

        # Write schema for reference (included in prompt)
        schema_path = tmpdir / "judge_schema.json"
        schema_path.write_text(json.dumps(JUDGE_SCHEMA, indent=2), encoding="utf-8")

        # Build a minimal workspace for Pi in the judge tmpdir.
        # The judge doesn't need Syke identity or a memex -- it's evaluating.
        # It needs: syke.db (empty) and adapters/ (from slice).
        judge_scope = trace_dir.parent.parent.name
        judge_workspace = (
            Path.home()
            / ".syke-lab"
            / judge_scope
            / "judges"
            / f"{run_id}__{uuid.uuid4().hex[:8]}"
            / "workspace"
        )
        if judge_workspace.exists():
            shutil.rmtree(judge_workspace)
        judge_workspace.mkdir(parents=True, exist_ok=True)
        (judge_workspace / "sessions").mkdir(exist_ok=True)
        _write_judge_determinism_extension(
            judge_workspace,
            judge_provider=resolve_pi_provider(judge_model),
        )

        # Copy slice adapters into the judge workspace
        slice_adapters = slice_dir / "adapters" if slice_dir.exists() else None
        judge_adapters = judge_workspace / "adapters"
        judge_adapters.mkdir(parents=True, exist_ok=True)
        if slice_adapters and slice_adapters.exists():
            for adapter in slice_adapters.glob("*.md"):
                shutil.copy2(adapter, judge_adapters / adapter.name)

        # Initialize syke.db (SykeDB constructor creates tables)
        db_path = judge_workspace / "syke.db"
        db = SykeDB(db_path)

        scratch_sessions = judge_workspace.parent / ".judge_sessions"

        prompt = _build_judge_prompt(
            packet_path=tmpdir / "packet.json",
            slice_path=tmpdir / "slice",
            local_git_anchor=local_git_anchor,
        )
        full_prompt = prompt

        try:
            with temporary_workspace_binding(
                judge_workspace,
                sessions_dir=scratch_sessions,
                harness_paths=slice_dir,
            ):
                answer_text_raw, metadata = pi_ask_module.pi_ask(
                    db,
                    "user",
                    full_prompt,
                    timeout=timeout_seconds,
                    model=judge_model,
                    transport="benchmark_judge",
                    capture_trace=True,
                )
                metadata = dict(metadata)

            # Write trace files
            trace_response = trace_dir / f"{run_id}.response.txt"
            trace_response.write_text(answer_text_raw, encoding="utf-8")
            trace_meta = trace_dir / f"{run_id}.metadata.json"
            trace_meta.write_text(json.dumps(metadata, indent=2, default=str), encoding="utf-8")
            (evidence_dir / "judge_response.txt").write_text(answer_text_raw, encoding="utf-8")
            (evidence_dir / "judge_metadata.json").write_text(
                json.dumps(metadata, indent=2, default=str), encoding="utf-8"
            )
            trace_payload = metadata.get("_trace_payload")
            if isinstance(trace_payload, dict):
                (trace_dir / f"{run_id}.judge_trace.json").write_text(
                    json.dumps(trace_payload, indent=2, default=str),
                    encoding="utf-8",
                )
                (evidence_dir / "judge_trace.json").write_text(
                    json.dumps(trace_payload, indent=2, default=str),
                    encoding="utf-8",
                )
            (evidence_dir / "index.json").write_text(
                json.dumps(
                    {
                        "probe_id": probe_id,
                        "condition": condition,
                        "slice_dir": str(slice_dir),
                        "packet_path": str(evidence_dir / "packet.json"),
                        "local_git_anchor": str(persistent_git_anchor)
                        if persistent_git_anchor
                        else None,
                    },
                    indent=2,
                ),
                encoding="utf-8",
            )

            result = _extract_tool_input(
                trace_payload if isinstance(trace_payload, dict) else None,
                tool_name="submit_judge_verdict",
            )
            if result is None:
                result = _extract_judge_json(answer_text_raw)
            if result is not None:
                final_path = trace_dir / f"{run_id}.final.json"
                final_path.write_text(json.dumps(result, indent=2), encoding="utf-8")
                (evidence_dir / "judge_result.json").write_text(
                    json.dumps(result, indent=2), encoding="utf-8"
                )
                return result

            LOG.warning("eval %s: could not extract valid judge JSON from Pi response", run_id)
            return {
                "error": f"JSON extraction failed from Pi response (len={len(answer_text_raw)})"
            }

        except Exception as exc:
            LOG.exception("eval %s: Pi judge failed", run_id)
            return {"error": repr(exc)[:500]}
        finally:
            db.close()
            # Clean up judge workspace
            judge_ws_parent = judge_workspace.parent
            if judge_ws_parent.exists():
                shutil.rmtree(judge_ws_parent, ignore_errors=True)


def _extract_judge_json(text: str) -> dict[str, Any] | None:
    """Extract judge JSON from Pi's response text.

    Tries direct parse first, then regex extraction of the outermost
    JSON object. Returns None if no valid JSON with required keys found.
    """
    required_keys = {"overall_verdict", "summary"}

    # Try direct parse
    stripped = text.strip()
    if stripped.startswith("{"):
        try:
            obj = json.loads(stripped)
            if isinstance(obj, dict) and required_keys.issubset(obj.keys()):
                return obj
        except json.JSONDecodeError:
            pass

    # Regex: find the outermost {...} block
    match = re.search(r"\{[\s\S]*\}", text)
    if match:
        try:
            obj = json.loads(match.group())
            if isinstance(obj, dict) and required_keys.issubset(obj.keys()):
                return obj
        except json.JSONDecodeError:
            pass

    return None


def _extract_tool_input(
    trace_payload: dict[str, Any] | None,
    *,
    tool_name: str,
) -> dict[str, Any] | None:
    if not isinstance(trace_payload, dict):
        return None
    tool_calls = trace_payload.get("tool_calls_detail")
    if not isinstance(tool_calls, list):
        return None
    for tool_call in reversed(tool_calls):
        if not isinstance(tool_call, dict):
            continue
        if str(tool_call.get("name") or "") != tool_name:
            continue
        payload = tool_call.get("input")
        if isinstance(payload, dict):
            return payload
    return None


def _reduce_verdict(judge_result: dict[str, Any]) -> str:
    """Map judge output to a single verdict: pass / partial / fail / invalid."""
    if "error" in judge_result:
        return "invalid"
    return str(judge_result.get("overall_verdict") or "fail")


def _persist_ask_trace(
    *,
    probe_id: str,
    condition: str,
    answer_text: str,
    answer_metadata: dict[str, Any],
    trace_dir: Path,
    evidence_dir: Path,
) -> dict[str, Any]:
    """Persist benchmark ask-side rollout traces without writing into syke.db."""
    trace_dir.mkdir(parents=True, exist_ok=True)
    evidence_dir.mkdir(parents=True, exist_ok=True)
    run_id = f"{condition}_{probe_id}"

    metadata = dict(answer_metadata)
    prompt_text = metadata.pop("_input_text", None)
    trace_payload = metadata.pop("_trace_payload", None)

    (trace_dir / f"{run_id}.ask_response.txt").write_text(answer_text, encoding="utf-8")
    (trace_dir / f"{run_id}.ask_metadata.json").write_text(
        json.dumps(metadata, indent=2, default=str),
        encoding="utf-8",
    )
    (evidence_dir / "ask_response.txt").write_text(answer_text, encoding="utf-8")
    (evidence_dir / "ask_metadata.json").write_text(
        json.dumps(metadata, indent=2, default=str),
        encoding="utf-8",
    )

    if prompt_text:
        (trace_dir / f"{run_id}.ask_prompt.txt").write_text(prompt_text, encoding="utf-8")
        (evidence_dir / "ask_prompt.txt").write_text(prompt_text, encoding="utf-8")

    if isinstance(trace_payload, dict):
        (trace_dir / f"{run_id}.ask_trace.json").write_text(
            json.dumps(trace_payload, indent=2, default=str),
            encoding="utf-8",
        )
        (evidence_dir / "ask_trace.json").write_text(
            json.dumps(trace_payload, indent=2, default=str),
            encoding="utf-8",
        )

    return metadata


# ── Core: evaluate one eval item ──


def evaluate_probe(
    *,
    item: dict[str, Any],
    condition: str,
    timeline: list[dict[str, Any]],
    ask_mode: str,
    bundle_path: Path,
    output_dir: Path,
    ask_timeout: int = 600,
    ask_model: str | None = None,
    judge_model: str | None = None,
    judge_timeout: int = 900,
    synthesis_path: Path | None = None,
) -> dict[str, Any]:
    """Evaluate a single (eval, condition) pair. Pure function.

    Builds a fresh workspace, injects cycle-matched memex, asks pi_ask,
    runs the Pi-based judge, returns the full result. No shared state.
    """
    probe_id = str(item["probe_id"])
    reference_moment = _reference_moment(item)
    memex_content = _find_cycle_matched_memex(timeline, reference_moment)

    LOG.info(
        "eval %s/%s: building workspace (memex=%d chars)", condition, probe_id, len(memex_content)
    )

    workspace_root, slice_dir = _build_probe_workspace(
        bundle_path=bundle_path,
        item=item,
        memex_content=memex_content,
        output_dir=output_dir,
        condition=condition,
    )

    try:
        trace_dir = output_dir / "traces" / condition
        evidence_dir = output_dir / "evidence" / condition / probe_id

        # Ask
        LOG.info("eval %s/%s: asking", condition, probe_id)
        answer_text, answer_metadata = _ask_probe(
            workspace_root=workspace_root,
            slice_dir=slice_dir,
            item=item,
            timeout_seconds=ask_timeout,
            ask_mode=ask_mode,
            ask_model=ask_model,
            synthesis_path=synthesis_path,
        )
        answer_metadata = _persist_ask_trace(
            probe_id=probe_id,
            condition=condition,
            answer_text=answer_text,
            answer_metadata=answer_metadata,
            trace_dir=trace_dir,
            evidence_dir=evidence_dir,
        )

        # Judge
        LOG.info("eval %s/%s: judging", condition, probe_id)
        judge_result = _judge_probe(
            item=item,
            answer_text=answer_text,
            answer_metadata=answer_metadata,
            slice_dir=slice_dir,
            condition=condition,
            judge_model=judge_model,
            timeout_seconds=judge_timeout,
            trace_dir=trace_dir,
            evidence_dir=evidence_dir,
        )

        verdict = _reduce_verdict(judge_result)

        return {
            "probe_id": probe_id,
            "dataset_id": item.get("dataset_id"),
            "family": item.get("family", ""),
            "prompt_text": item.get("prompt_text") or item.get("question", ""),
            "condition": condition,
            "ask_mode": ask_mode,
            "reference_dt": reference_moment.strftime("%Y-%m-%d"),
            "reference_ts_local": item.get("reference_ts_local"),
            "memex_chars": len(memex_content),
            "answer_text": answer_text,
            "answer_metadata": answer_metadata,
            "tool_calls": int(answer_metadata.get("tool_calls") or 0),
            "cost_usd": float(answer_metadata.get("cost_usd") or 0.0),
            "zero_search": int(answer_metadata.get("tool_calls") or 0) == 0,
            "efficiency": {
                "tool_calls": int(answer_metadata.get("tool_calls") or 0),
                "cost_usd": float(answer_metadata.get("cost_usd") or 0.0),
                "duration_ms": int(answer_metadata.get("duration_ms") or 0),
                "num_turns": int(answer_metadata.get("num_turns") or 0),
                "zero_search": int(answer_metadata.get("tool_calls") or 0) == 0,
            },
            "artifacts": {
                "slice_dir": str(slice_dir),
                "evidence_dir": str(evidence_dir),
                "trace_dir": str(trace_dir),
            },
            "judge_result": judge_result,
            "verdict": verdict,
            "completed_at": _now_iso(),
        }

    except (KeyboardInterrupt, SystemExit):
        raise
    except Exception as exc:
        LOG.exception("eval %s/%s: failed", condition, probe_id)
        return {
            "probe_id": probe_id,
            "dataset_id": item.get("dataset_id"),
            "family": item.get("family", ""),
            "prompt_text": item.get("prompt_text") or item.get("question", ""),
            "condition": condition,
            "ask_mode": ask_mode,
            "reference_dt": reference_moment.strftime("%Y-%m-%d"),
            "reference_ts_local": item.get("reference_ts_local"),
            "memex_chars": len(memex_content),
            "answer_text": "",
            "answer_metadata": {},
            "tool_calls": 0,
            "cost_usd": 0.0,
            "zero_search": False,
            "efficiency": {
                "tool_calls": 0,
                "cost_usd": 0.0,
                "duration_ms": 0,
                "num_turns": 0,
                "zero_search": False,
            },
            "artifacts": {
                "slice_dir": str(slice_dir),
                "evidence_dir": str(output_dir / "evidence" / condition / probe_id),
                "trace_dir": str(output_dir / "traces" / condition),
            },
            "judge_result": {},
            "verdict": "invalid",
            "error": repr(exc),
            "completed_at": _now_iso(),
        }
    finally:
        # Clean up per-eval workspace (slices kept for audit).
        # workspace_root is ~/.syke-lab/<run>/<condition>__<probe_id>/workspace/;
        # remove the parent dir so nothing accumulates under ~/.syke-lab/.
        if workspace_root.parent.exists():
            shutil.rmtree(workspace_root.parent, ignore_errors=True)


def _evaluate_single_condition(
    *,
    item: dict[str, Any],
    condition: str,
    spec: dict[str, Any],
    output_dir: Path,
    ask_timeout: int,
    ask_model: str | None,
    judge_model: str | None,
    judge_timeout: int,
) -> dict[str, Any]:
    routing_ds = _routing_dataset_id(item)
    bundle_path = _bundle_for_dataset(routing_ds, primary_dataset_id=item.get("primary_dataset_id"))
    synthesis_path: Path | None = None
    skill_content = spec.get("skill_content")
    if str(spec.get("ask_mode") or "") == "syke" and isinstance(skill_content, str) and skill_content:
        skills_dir = output_dir / ".condition_skills"
        skills_dir.mkdir(parents=True, exist_ok=True)
        safe_name = re.sub(r"[^A-Za-z0-9._-]+", "_", condition)
        synthesis_path = skills_dir / f"{safe_name}.md"
        synthesis_path.write_text(skill_content, encoding="utf-8")
    return evaluate_probe(
        item=item,
        condition=condition,
        timeline=list(spec.get("timeline") or []),
        ask_mode=str(spec.get("ask_mode") or "syke"),
        bundle_path=bundle_path,
        output_dir=output_dir,
        ask_timeout=ask_timeout,
        ask_model=ask_model,
        judge_model=judge_model,
        judge_timeout=judge_timeout,
        synthesis_path=synthesis_path,
    )


# ── CLI + main ──


def _parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="NE-1 Coherence benchmark")
    parser.add_argument("--runset", help="Named probe set from the active runsets YAML")
    parser.add_argument("--item", action="append", help="Explicit probe ID(s)")
    parser.add_argument(
        "--all-items", action="store_true", help="Run every item from the selected items file"
    )
    parser.add_argument("--items-file", help="Override benchmark items YAML path")
    parser.add_argument("--runsets-file", help="Override benchmark runsets YAML path")
    parser.add_argument(
        "--output-dir",
        help=(
            "Run output directory. Default: "
            "_internal/syke-replay-lab/runs/<slug>-<UTC-stamp>/ "
            "where <slug> is the runset name (or 'items'/'run')."
        ),
    )
    parser.add_argument(
        "--replay-dir",
        action="append",
        help="condition:path pairs using canonical conditions only (e.g. production:runs/timefix/production)",
    )
    parser.add_argument("--ask-model", help="Ask/runtime model override")
    parser.add_argument("--judge-model", default="gpt-5.4", help="Judge model")
    parser.add_argument("--ask-timeout", type=int, default=600, help="Ask timeout (s)")
    parser.add_argument("--judge-timeout", type=int, default=900, help="Judge timeout (s)")
    parser.add_argument("--max-items", type=int, help="Cap evals (smoke runs)")
    parser.add_argument("--jobs", type=int, default=1, help="Parallel eval-item workers")
    return parser.parse_args()


def main() -> None:
    logging.basicConfig(
        level=logging.INFO, format="%(asctime)s %(levelname)s %(name)s: %(message)s"
    )
    args = _parse_args()
    if args.output_dir:
        output_dir = Path(args.output_dir).resolve()
    else:
        # Default: land under the lab's runs/ so the viz auto-discovers the
        # new run without any manifest edit. Slug prefers the runset name;
        # falls back to 'items' (explicit --item list) or 'run'.
        stamp = datetime.now(UTC).strftime("%Y%m%dT%H%M%SZ")
        if args.runset:
            slug = args.runset
        elif args.item:
            slug = f"items-{len(args.item)}"
        else:
            slug = "run"
        output_dir = (LAB_ROOT / "runs" / f"{slug}-{stamp}").resolve()
        LOG.info("No --output-dir given; using %s", output_dir)
    output_dir.mkdir(parents=True, exist_ok=True)
    ask_model = args.ask_model or _default_benchmark_model()

    # Load conditions from --replay-dir flags
    # Format: canonical_condition:path.
    # `pure` is always injected as the null baseline and never points at a replay source.
    conditions: dict[str, dict[str, Any]] = {}
    for entry in args.replay_dir or []:
        if ":" in entry:
            cond, path_str = entry.rsplit(":", 1)
            try:
                cond = _canonical_condition_name(cond)
            except ValueError as exc:
                LOG.error(str(exc))
                sys.exit(1)
            if cond == "pure":
                LOG.error("Pure is always included automatically and must not point at a replay source.")
                sys.exit(1)
            try:
                conditions[cond] = _load_replay_condition_spec(
                    condition=cond,
                    replay_dir=Path(path_str).resolve(),
                    synthesis_path=None,
                )
            except ValueError as exc:
                LOG.error(str(exc))
                sys.exit(1)
        else:
            try:
                cond = _canonical_condition_name(entry)
            except ValueError as exc:
                LOG.error(str(exc))
                sys.exit(1)
            if cond != "pure":
                LOG.error(
                    "Bare condition names are only allowed for `pure`. Non-baseline eval "
                    "conditions must point at a replay source with condition:path."
                )
                sys.exit(1)
            conditions[cond] = {
                "timeline": [],
                "ask_mode": _ask_mode_for_condition(cond),
                "replay_source": None,
                "synthesis_path": None,
            }

    conditions.setdefault(
        "pure",
        {
            "timeline": [],
            "ask_mode": "pure",
            "replay_source": None,
            "synthesis_path": None,
        },
    )

    # Load eval items
    items_path = Path(args.items_file).resolve() if args.items_file else None
    runsets_path = Path(args.runsets_file).resolve() if args.runsets_file else None
    selected = _select_items(
        args.item,
        args.runset,
        items_path=items_path,
        runsets_path=runsets_path,
        all_items=bool(args.all_items),
    )
    runnable: list[dict[str, Any]] = []
    for item in selected:
        status = str(item.get("status") or "").strip()
        if status in ("blocked-deadzone", "needs-cross-bundle-merge"):
            LOG.info("skipping eval %s (%s)", item["probe_id"], status)
            continue
        runnable.append(item)
    if args.max_items:
        runnable = runnable[: args.max_items]

    LOG.info(
        "Evaluating %d items × %d conditions = %d evaluations",
        len(runnable),
        len(conditions),
        len(runnable) * len(conditions),
    )

    # Config (for provenance)
    config = {
        "started_at": _now_iso(),
        "probes": [item["probe_id"] for item in runnable],
        "conditions": [
            {
                "name": name,
                "ask_mode": spec["ask_mode"],
                "replay_source": spec["replay_source"],
                "synthesis_path": spec.get("synthesis_path"),
            }
            for name, spec in conditions.items()
        ],
        "ask_model": ask_model,
        "judge_model": args.judge_model,
        "ask_timeout": args.ask_timeout,
        "jobs": args.jobs,
        "items_file": str(items_path) if items_path else str(ITEMS_PATH),
        "runsets_file": str(runsets_path) if runsets_path else str(RUNSETS_PATH),
    }
    _write_json_atomic(output_dir / "config.json", config)

    # Checkpoint: load existing results to skip completed evals
    results_path = output_dir / "results.json"
    all_results: list[dict[str, Any]] = []
    completed_keys: set[str] = set()
    if results_path.exists():
        all_results = json.loads(results_path.read_text(encoding="utf-8"))
        completed_keys = {f"{r['condition']}_{r['probe_id']}" for r in all_results}
        LOG.info("Resuming: %d evaluations already completed", len(completed_keys))

    pending_evals: list[tuple[dict[str, Any], str, dict[str, Any]]] = []
    for item in runnable:
        probe_id = str(item["probe_id"])
        for condition, spec in conditions.items():
            key = f"{condition}_{probe_id}"
            if key in completed_keys:
                LOG.info("%s: already done, skipping", key)
                continue
            pending_evals.append((item, condition, spec))

    # Evaluate
    if args.jobs <= 1:
        for item, condition, spec in pending_evals:
            result = _evaluate_single_condition(
                item=item,
                condition=condition,
                spec=spec,
                output_dir=output_dir,
                ask_timeout=args.ask_timeout,
                ask_model=ask_model,
                judge_model=args.judge_model,
                judge_timeout=args.judge_timeout,
            )
            all_results.append(result)
            completed_keys.add(f"{result['condition']}_{result['probe_id']}")
            _write_json_atomic(results_path, all_results)
            print(f"{result['condition']}/{result['probe_id']}: verdict={result['verdict']}")
    else:
        with concurrent.futures.ProcessPoolExecutor(max_workers=args.jobs) as executor:
            future_map = {
                executor.submit(
                    _evaluate_single_condition,
                    item=item,
                    condition=condition,
                    spec=spec,
                    output_dir=output_dir,
                    ask_timeout=args.ask_timeout,
                    ask_model=ask_model,
                    judge_model=args.judge_model,
                    judge_timeout=args.judge_timeout,
                ): f"{condition}_{item['probe_id']}"
                for item, condition, spec in pending_evals
            }
            for future in concurrent.futures.as_completed(future_map):
                eval_key = future_map[future]
                try:
                    result = future.result()
                except Exception as exc:
                    LOG.exception("eval %s: worker failed", eval_key)
                    print(f"{eval_key}: worker failed ({exc!r})")
                    continue
                all_results.append(result)
                completed_keys.add(f"{result['condition']}_{result['probe_id']}")
                _write_json_atomic(results_path, all_results)
                print(f"{result['condition']}/{result['probe_id']}: verdict={result['verdict']}")

    # Summary
    summary = _summarize_results(all_results)
    print(
        "\n"
        f"{'condition':16s} pass  partial  fail  invalid  SR     ZSSR   "
        "tok/success  sec/success  avg_tc  avg_tok  avg_sec"
    )
    print("-" * 118)
    for cond, row in sorted(summary.items()):
        counts = row["counts"]
        print(
            f"{cond:16s} "
            f"{counts.get('pass', 0):4d}  "
            f"{counts.get('partial', 0):7d}  "
            f"{counts.get('fail', 0):4d}  "
            f"{counts.get('invalid', 0):7d}  "
            f"{row['success_rate']:.2f}  "
            f"{row['zero_search_success_rate']:.2f}  "
            f"{row['tokens_per_success']:.1f}      "
            f"{row['duration_ms_per_success'] / 1000:.1f}         "
            f"{row['avg_tool_calls']:.1f}    "
            f"{row['avg_total_tokens']:.1f}    "
            f"{row['avg_duration_ms'] / 1000:.1f}"
        )

    config["completed_at"] = _now_iso()
    config["total_evaluations"] = len(all_results)
    _write_json_atomic(output_dir / "config.json", config)
    canonical_results = {
        "items": all_results,
        "summary": summary,
        "config": config,
    }
    canonical_results_path = output_dir / "benchmark_results.json"
    _write_json_atomic(canonical_results_path, canonical_results)
    print(f"\nResults checkpoint: {results_path}")
    print(f"Canonical results: {canonical_results_path}")


